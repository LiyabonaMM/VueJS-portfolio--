<template>
  <section class="work-education">
    <div class="container">
      <h2>Resume</h2>
      <div class="timeline">
        <div
          class="timeline-item"
          v-for="experience in experiences"
          :key="experience.id"
        >
          <div class="timeline-content">
            <h3>{{ experience.title }}</h3>
            <p class="timeline-date">{{ experience.duration }}</p>
            <ul class="timeline-description">
              <li
                v-for="description in experience.descriptions"
                :key="description"
              >
                {{ description }}
              </li>
            </ul>
          </div>
        </div>
      </div>
    </div>
  </section>
</template>

<script>
import educationData from '@/data/education.json';

export default {
  name: 'WorkEducation',
  data() {
    return {
      experiences: []
    };
  },
  async mounted() {
    try {
      this.experiences = await this.fetchExperienceData();
    } catch (error) {
      console.error('Failed to fetch experience data:', error);
    }
  },
  methods: {
    fetchExperienceData() {
      return new Promise((resolve, reject) => {
        // Simulating an asynchronous API call
        setTimeout(() => {
          try {
            // Assuming educationData is an array from the education.json file
            resolve(educationData);
          } catch (error) {
            reject(error);
          }
        }, 1000);
      });
    }
  }
};
</script>

